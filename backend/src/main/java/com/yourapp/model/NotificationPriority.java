package com.yourapp.model;

/**
 * Перечисление приоритетов уведомлений
 */
public enum NotificationPriority {
    /**
     * Критические уведомления (просроченные задачи, важные упоминания)
     */
    CRITICAL,
    
    /**
     * Высокий приоритет (назначения задач, приглашения на доски)
     */
    HIGH,
    
    /**
     * Обычный приоритет (новые задачи, комментарии)
     */
    NORMAL,
    
    /**
     * Низкий приоритет (информационные уведомления, изменения статусов)
     */
    LOW
} 